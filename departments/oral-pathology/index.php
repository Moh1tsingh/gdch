            <h1>UG CURRICULUM</h1>
            <p>The undergraduate students in this department include the First Year and the Third Year
                students for the subjects Dental Anatomy & Dental Histology, and Oral Pathology & Oral
                Microbiology respectively.</p>
            <h2>DENTAL ANATOMY, EMBRYOLOGY AND ORAL HISTOLOGY</h2>
            <p>Dental Anatomy including Embryology and Oral Histology – a composite of basic Dental Sciences &
            their clinical applications.</p>
            <h2>Skills</h2>
            <p>The student should acquire basic skills in :</p>
            <li>1. Carving of crowns of permanent teeth in wax.</li>
            <li>2. Microscopic study of Oral tissues.</li>
            <li>3. Identification of Deciduous & Permanent teeth.</li>
            <li>4. Age estimation by patterns of teeth eruption from plaster casts of different age groups.</li>
            <h2>Objectives  </h2>
            <li>1. The student is expected to appreciate the normal development, morphology, structure & functions of oral
            tissues & variations in different pathological/non-pathological states.</li>
            <li>2. The student should understand the histological basis of various dental treatment procedures and
            physiologic ageing process in the dental tissues.</li>
            <li>3. The students must know the basic knowledge of various research methodologies.</li>
            <h2>Tooth morphology</h2>
            <li>Detailed microscopic study of Enamel, Dentine, Cementum & Pulp tissue and their Age changes &
            Applied aspects</li>
            <li>Detailed microscopic study of Periodontal ligament & alveolar bone, age changes, histological changes
            in periodontal ligament & bone in normal & orthodontic tooth movement, applied aspects of alveolar
            bone resorption</li>
            <li>Detailed microscopic study of Oral Mucosa</li>
            <li>Salivary Glands</li>
            <li>TM Joint : Review of basic anatomical aspects & microscopiuc study & clinical considerations.</li>
            <li>Maxillary Sinus</li>
            <li>Processing of Hard & soft tissues for microscopic study, Ground sections, decalcified sections & routine
            staining procedures.</li>
            <li>Basic histochemical staining patterns of oral tissues.</li>
            <h2>ORAL PATHOLOGY & ORAL MICROBIOLOGY</h2>
            <h2>Introduction:</h2>
            <p>A bird's eye view of the different pathological processes involving the oral cavity & oral cavity
            involvement in systemic diseases to be brought out. Interrelationship between General Medicine &
            General Surgery & Oral pathology to be emphasized.</p>
            <h2>Objectives</h2>
            <li>Students should have knowledge about Tthe different types of pathological processes, that involve the oral
            cavity.</li>
            <li>The manifestations of common diseases, their diagnosis & correlation with clinical pathological processes.</li>
            <li>An understanding of the oral manifestations of systemic diseases should help in correlating with the
            systemic physical signs & laboratory findings.</li>
            <li>The student should understand the underlying biological principles governing treatment of oral diseases.</li>
            <li>The principles of certain basic aspects of Forensic Odontology.</li>
            <h2>Skills:</h2>
            <li>Microscopic study of common lesions affecting oral tissues through microscopic slides & projection slides.</li>
            <li>Study of the disease process by surgical specimens.</li>
            <li>Study of teeth anomalies/polymorphisms through tooth specimens & plaster casts.</li>
            <li>Microscopic study of plaque pathogens.</li>
            <li>Study of haematological preparations (blood films) of anaemias & leukemias.</li>
            <li>Basic exercises in Forensic Odontology such as histological methods of age estimation and appearance of
            teeth in injuries</li>
            <h2>Oral Pathologies:</h2>
            <li>Developmental disturbances of teeth, jaws and soft tissues of oral & paraoral region</li>
            <li>Forensic Odontology</li>
            <li>Dental Caries</li>
            <li>Periodontal Diseases</li>
            <li>Microbiology: Bacterial, Viral, Fungal</li>
            <li>Common non-inflammatory diseases involving the jaws</li>
            <li>Diseases of TM Joint</li>
            <li>Tumours of the Oral Cavity: Odontogenic, Non-Odontogenic & Salivary Gland Tumours, Benign Epithelial, Benign Mesenchymal, Malignant Epithelial, Malignant Mesenchymal & Other Reticuloendothelial tumours</li>
            <li>Salivary Gland - Benign Epithelial neoplasms & Malignant Epithelial neoplasms</li>
            <li>Metastatic tumours - Tumors metastasizing to & from oral cavity & the routes of metastasis</li>
            <li>Traumatic, Reactive & Regressive lesions of Oral Cavity</li>
            <li>Attrition, Abrasion, Erosion, Bruxism, Hypercementosis, Dentinal changes, Pulp calcifications & Resorption of teeth</li>
            <li>Radiation effects of oral cavity</li>
            <li>Physical & Chemical injuries including allergic reactions of the oral cavity</li>
            <li>Healing of Oral wounds & complications - Dry socket</li>
            <li>Non neoplastic Salivary Gland Diseases</li>
            <li>Brief review & oral manifestations, diagnosis & significance of common Blood, Nutritional, Hormonal & Metabolic diseases of Oral cavity</li>
            <li>Mucocutaneous Lesions</li>
            <li>Facial neuralgias - Trigeminal & Glossopharyngeal. VII nerve paralysis, Causalgia</li>
            <li>Psychogenic facial pain & Burning mouth syndrome</li>
            <li>Pigmentation of Oral & Paraoral region & Discolouration of teeth</li>
            <li>Oral precancer – cancer; Epidemiology, aetiology, clinical and histopathological features, TNM classification. Recent advances in diagnosis, management and prevention</li>
            <li>Biopsy: Types of biopsy, value of biopsy, cytology, histochemistry & frozen sections in diagnosis of oral diseases</li>
        <hr>
