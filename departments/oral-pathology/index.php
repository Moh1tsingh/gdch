            <p>Practical : 80%</p>
            <h2>INTERNSHIP</h2>
            <li>1. History-recording and clinical examination - 5 Cases</li>
            <li>2. Blood, Urine and Sputum examination - 5 Cases</li>
            <li>3. Exfoliative Cytology and smears study - 2 Cases</li>
            <li>4. Biopsy- Laboratory Procedure & reporting - 1 Case</li>
            <h2>EVALUATION</h2>
            <p>Evaluation may be achieved by the following tested methods:</p>
            <li>1. Written test</li>
            <li>2. Practicals</li>
            <li>3. Clinical examination</li>
            <li>4. Viva Voce</li>
            <li>5. Jounrals- Dental Anatomy & Dental Histology</li>
            <h2>Internal assessment examination</h2>
            <p>The continuing assessment examinations are held 3 times in a particular year and the average marks of
            these examinations are considered. 10% of the total marks in each subject for both theory, practical and
            clinical examination separately are set aside for the internal assessment examinations.</p>
