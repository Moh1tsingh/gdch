            <h1>PG CURRICULUM</h1>
            <h2>GOALS</h2>
            <p>The goals of the post-graduate training in various specialities is to train the graduate in Dental Surgery
who will,</p>
<li><span class="bold-solid-li">Lectures:</span> There shall be some didactic lectures in the speciality and in the allied fields. The departments shall encourage guest lectures in the required areas and integrated lectures by multi-disciplinary teams on selected topics, to strengthen the training programmes</li>
  <li><span class="bold-solid-li">Journal review:</span> The journal review meetings shall be held at least once a week. All trainees, associate and staff associated with the post-graduate programme are expected to participate actively and enter relevant details in the logbook. The trainee shall make presentations from the allotted journals of selected articles. A model checklist for the evaluation of journal review presentation is annexed at Schedule-I of these regulations</li>
  <li><span class="bold-solid-li">Seminars:</span> The seminars shall be held at least twice a week in each department. All trainees are expected to participate actively and enter relevant details in the logbook. A model checklist for the evaluation of seminar presentation is annexed at Schedule-II of these regulations</li>
  <li><span class="bold-solid-li">Symposium:</span> It is recommended to hold symposium on topics covering multiple disciplines</li>
  <li><span class="bold-solid-li">Clinical postings:</span> Each trainee shall work in the clinics on a regular basis to acquire adequate professional skills and competency in managing various cases. A model checklist for the evaluation of clinical postings is annexed at Schedule-III of these regulations</li>
  <li><span class="bold-solid-li">Clinicopathologic conferences:</span> The clinico pathological conference shall be held once a month involving the faculties of Oral Medicine and Radiology, Oral Pathology and allied clinical departments. The trainees shall be encouraged to present the clinical details, radiological and histo-pathological interpretations, and participate in the discussions</li>
  <li><span class="bold-solid-li">Inter-departmental meetings:</span> To encourage integration among various specialities, there shall be an inter-departmental meeting chaired by the Dean with all heads of post-graduate departments at least once a month</li>
  <li><span class="bold-solid-li">Teaching skills:</span> All the trainees shall be encouraged to take part in undergraduate teaching programmes either in the form of lectures or group discussions. A model checklist for the evaluation of teaching skills is annexed at Schedule-IV of these regulations</li>
  <li><span class="bold-solid-li">Dental education programmes:</span> Each department shall organise dental education programmes on a regular basis involving other institutions. The trainees shall also be encouraged to attend such programmes conducted outside their university or institute</li>
  <li><span class="bold-solid-li">Conferences / Workshops / Advanced courses:</span> The trainees shall be encouraged to attend conferences, workshops, advanced courses, and also to present at least two scientific papers and two posters at State / national level speciality and allied conferences / conventions during the training period</li>
  <li><span class="bold-solid-li">Rotation and posting in other departments:</span> To bring in more integration among the specialities and allied fields, each department shall work out a programme to rotate the trainees in related disciplines</li>
  <li><span class="bold-solid-li">Dissertation / thesis:</span> The trainees shall prepare a dissertation based on the clinical or experimental work or any other study conducted by them under the supervision of the guide. A model checklist for the evaluation of dissertation presentation and continuous evaluation of dissertation work by the guide / co-guide is annexed at Schedule-V of these regulations. A model overall assessment sheet to be filled by all the trainees undergoing post-graduate course is annexed at Schedule-VI of these regulations</li>
  <h2>MINIMUM QUOTA</h2>
  <p>All the students of the speciality departments shall complete the minimum quota for the teaching and
learning activities, as follows:-</p>
<li>Journal Clubs: 5 in a year</li>
  <li>Seminars: 5 in a year</li>
  <li>Clinical Case Presentations: 4 in a year</li>
  <li>Lectures taken for undergraduates: 1 in a year</li>
  <li>Scientific Paper / Poster Presentations: 4 papers/posters during State / National Level Conferences / three years of training workshop period</li>
  <li>Clinico Pathological Conferences: 2 presentations during three years of training period</li>
  <li>Scientific Publications (optional): one publication in any indexed scientific journal</li>
  <li>Submission of Synopsis: one synopsis within six months from the date of commencement of the course</li>
  <li>Submission of Dissertation: one dissertation within six months before appearing for the university examination</li>
  <li>Submission of Library Dissertation / Systematic Review: one dissertation within eighteen months from the date of commencement of the course</li>
