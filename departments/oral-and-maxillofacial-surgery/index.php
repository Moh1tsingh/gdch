            </p>
            <p>
                4.Patients are given appointments for extraction by the Dental surgeons. Medically compromised patients are evaluated by the staff as well as the anaesthesiologist posted in the department.
            </p>
            <p>
                5. Patients who report for extraction are allotted to undergraduate students and interns to carry out extractions. Patients are monitored closely after extraction. Once the patient is stable, they are discharged. Case papers are signed by Associate professors/Assistant professors following completion of the procedure. Medically compromised patients are treated under the supervision of the anaesthetist.
            </p>
            <p>
                6. Special cases such as Trauma, Benign and Malignant tumours, Premalignant conditions, TMJ disorders, Sero-positivepatients, Space infection cases etc are referred to the PG section ie. room no. 100
            </p>
            <h2>
                PG Clinic
            </h2>
            <p>
                1. Special cases are evaluated in the PG clinic initially by the Post graduate students.
            </p>
            <p>
                2. Clinical findings, Medical history, Deleterious habits history, Previous dental history etc are noted down on the case paper by Post graduate students.
            </p>
            <p>
                3. All cases are discussed with Guide and Unit head to formulate a treatment plan. Necessary radiographs and blood investigations are advised. Patients requiring major surgery are admitted in the wards at Govt. Medical hospital, Aurangabad and are considered for surgery under General Anaesthesia / Local anaesthesia in the Operation theatre.
            </p>
            <p>
                4. Minor surgeries are carried out by postgraduate students under the guidance of their guide and concern unit.
            </p>
            <p>
                5. Major cases are treated by faculty members and it is assisted by post graduate students.
            </p>
            <p>
                6. Patients who require prolonged monitoring are admitted in the wards at Govt. Medical hospital, Aurangabad. Daily rounds are taken by Unit head and operating surgeon with a post graduate student at Govt. Medical hospital, Aurangabad.
            </p>
