            <h2>
                PATIENT SERVICES
            </h2>
            <h3>
                Oral Medicine:
            </h3>
            <p>
                The services provided by the department in this regards are as follows:
            </p>
            <li>
                Diagnostic assessment of common dental conditions by history and clinical examination of the patients attending the OPD.Investigations of the patients if warranted and medicinal treatment if required.
            </li>
            <li>
                Provision of screening and counselling services for tobacco and betel-nut addiction cessation, supported by a dedicated tobacco cessation centre.
            </li>
            <li>
                Screening and diagnosis of head and neck malignancies.
            </li>
            <li>
                Management of complications arising from chemotherapy and radiotherapy.
            </li>
            <li>
                Identification and management of diverse oral mucosal conditions, potentially malignant disorders, salivary gland disorders, autoimmune diseases, and pigmented lesions of the oral mucosa.
            </li>
            <li>
                Treatment of TemporomandibularJoint disorders and various Orofacial pain syndromes.
            </li>
            <li>
                Diagnosis and pharmacological treatment of oral manifestations associated with systemic illnesses.
            </li>
            <li>
                Treatment of psychosomatic disorders and neurological conditions impacting the jaws and oral cavity. 
            </li>
            <h3>
                Oral Radiology:
            </h3>
            <p>
                Oral Radiology specialty deals with the production and interpretations of the images and data produced by all modalities of radiant energy that are used for the diagnosis of disorders or conditions affecting the oral and maxillofacial regions. The services provided by the department in this regards are as follows:
            </p>
            <li>
                Intraoral radiography including Intra Oral Periapical , Bitewing and Occlusal projections.
            </li>
            <li>
                Extraoral radiography including digital OPG, lateral cephalograms and various skull projections like postero-anterior (PA), antero-posterior (AP), lateral skull views,submento-vertex view and TMJ views.
            </li>
            <li>
                Digital imaging: Radiovisiography and Phosphor Plate Imaging 
            </li>
            <li>
                Advanced imaging: Cone beam computed tomography (CBCT) .
            </li>
